namespace Backend_modul_1_Code_challenges__Fundament_.Classes.Calculator;

public class CalculatorMath
{
    public double AddNumbers(double a, double b)
    {
        return a + b;
    }

    public double SubtractNumbers(double a, double b)
    {
        return a - b;
    }

    public double MultiplyNumber(double a, double b)
    {
        return a * b;
    }

    public double DivisionNumber(double a, double b)
    {
        return a / b;
    }
}
